class MinStack {
public:
    vector<int>v;
    MinStack() {
        
    }
    
    void push(int val) {
        v.push_back(val);
    }
    
    void pop() {
        v.pop_back();
    }
    
    int top() {
        return v[v.size()-1];
    }
    
    int getMin() {
        int mini=INT_MAX;
        for(auto it:v){
            if(it<mini)mini=it;
        }
        return mini;
    }
};

